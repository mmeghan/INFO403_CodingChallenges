def ProcessAdjacencyList(adjacency_list):
    adj_list = {}
    circuit_max = 0
    for edge in adjacency_list:
        edge = edge.replace(' -> ', ' ')
        edge = edge.split(' ')
        adj_list.setdefault(edge[0], [])
        for node in edge[1].split(','):
            adj_list[edge[0]].append(node)
            circuit_max += 1
    
    return (adj_list, circuit_max)

